console.log("Je commence à compter avec une boucle for:");

for (let i = 1; i <= 10; i++) {
    console.log(i);
}

console.log("Fin du compte !");



console.log("Je commence à compter avec une boucle while:");

let nbr = 1;

while (nbr <= 10) {
    console.log(nbr);
    nbr++;
}

console.log("Fin du compte !");